const dompurify = require("dompurify");

///////////////////////////////////////////////////////////////////////////////
// STRING VALIDATION
///////////////////////////////////////////////////////////////////////////////

/**
 * DOMPurify wrapper.
 * @param {string} stringToSanitize input string to sanitize.
 */
function DOMSanitize(stringToSanitize) {
    return dompurify.sanitize(stringToSanitize);
}


///////////////////////////////////////////////////////////////////////////////
// MODULE EXPORT
///////////////////////////////////////////////////////////////////////////////

module.exports = {
    DOMSanitize: DOMSanitize
};